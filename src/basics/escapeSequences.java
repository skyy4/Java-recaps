package basics;

public class escapeSequences {
    public static void main(String[] args) {
        //for " "
        System.out.println("Hello \"Sky\" how are \"u\"?");
        //for dir like c:\windows\..
        System.out.println("c:\\windows\\...");
        // new line = \n
        System.out.println("sky\nis");
        // tab
        System.out.println("\tgreat");

    }
}
